-- Fix: 稳固护盾 (Stable Shields, 16261/16290/16291)
--       仅对闪电护盾生效，对大地之盾/水源护盾无效
-- 根因:
--   EffectItemType=1024 (int32, 仅 bit10 = 闪电护盾)
--   大地之盾(bit40) 和 水源护盾(bit38/43) 的位超过32位，
--   无法存储在 EffectItemType(int32) 中
-- 修复:
--   在 spell_affect 表中插入 64 位组合掩码条目
--   组合掩码: bit10(闪电) | bit38(水源被动) | bit40(大地) | bit43(水源施放)
--   = 0x94000000400 = 10170482557952
-- GENERATED: 20260608225300
-- ==============================================

-- 为三个等级添加 spell_affect 条目（effectId=0 充能, effectId=1 冷却）
-- 使用 64 位 SpellFamilyMask 覆盖 EffectItemType 的 32 位限制
INSERT INTO `spell_affect` (`entry`, `effectId`, `SpellFamilyMask`) VALUES
(16261, 0, 10170482557952),
(16261, 1, 10170482557952),
(16290, 0, 10170482557952),
(16290, 1, 10170482557952),
(16291, 0, 10170482557952),
(16291, 1, 10170482557952);
